const languageDefineds: Record<string, LanguageDefined> = {};

[TSql, Odbc, MySql, Oracle, Es5, Java, CSharp, Python, Php, Go, Rust].forEach(
  (ld) => (languageDefineds[ld.name.toUpperCase()] = ld)
);

/**
 * Get all lanugage name list
 * @returns langauge name array
 */
export function getLangaugeNames() {
  return Object.keys(languageDefineds);
}

export function registerReservedKeywords(languageDefined: LanguageDefined) {
  console.assert(languageDefined != null);
  console.assert(typeof languageDefined.name === "string");
  languageDefineds[languageDefined.name.toUpperCase()] = languageDefined;
}

function check(
  word: string,
  languageDefined: LanguageDefined,
  ignorecase = false
): boolean {
  if (!word) {
    return false;
  }
  if (ignorecase || languageDefined.ignorecase) {
    return languageDefined.keywords.some(
      (kw) => word.toUpperCase() === kw.toUpperCase()
    );
  } else {
    return languageDefined.keywords.includes(word);
  }
}

/**
 * Check reserved keywords
 * @param word A word for check reserved keywords
 * @param languages Check language names
 * @param ignorecase Force ignore case equals
 * @return `word` is a reserved keyword at language name array or `false`
 */
export function checkReservedKeywords(
  word: string,
  languages: (
    | KnownLanguageName
    | string
    | LanguageDefined
  )[] = getLangaugeNames(),
  ignorecase = false
): string[] | false {
  const result: string[] = [];

  for (const lang of languages) {
    if (typeof lang === "string") {
      if (lang.toUpperCase() in languageDefineds) {
        if (check(word, languageDefineds[lang.toUpperCase()], ignorecase)) {
          result.push(lang.toUpperCase());
        }
      }
    } else if (check(word, lang, ignorecase)) {
      result.push(lang.name);
    }
  }

  if (result.length) {
    return result;
  }
  return false;
}

type KnownLanguageName =
  | "T-SQL"
  | "MYSQL"
  | "ORACLE"
  | "ODBC"
  | "ES5"
  | "ES6"
  | "JAVA"
  | "CSHARP"
  | "PYTHON"
  | "GO"
  | "RUST";

/**
 * Check reserved keywords
 * @param word A word for check reserved keywords
 * @param languages Check language names
 * @param ignorecase Force ignore case equals
 * @return `word` is a reserved keyword
 */
export function isReservedKeywords(
  word: string,
  languages: (
    | KnownLanguageName
    | string
    | LanguageDefined
  )[] = getLangaugeNames(),
  ignorecase = false
): boolean {
  for (const lang of languages) {
    if (typeof lang === "string") {
      if (lang.toUpperCase() in languageDefineds) {
        if (check(word, languageDefineds[lang.toUpperCase()], ignorecase)) {
          return true;
        }
      }
    } else if (check(word, lang, ignorecase)) {
      return true;
    }
  }
  return false;
}
